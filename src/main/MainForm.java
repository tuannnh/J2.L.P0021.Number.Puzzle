    private void congratulate() {
        running = false;
        String time = lblTime.getText();
        CongratulateForm congratulateForm = new CongratulateForm("" + moves, time);
        congratulateForm.setVisible(true);
        congratulateForm.setLocationRelativeTo(this);
        if (cbboxSize.getSelectedIndex() == 0) {
            for (JButton button : listOf3x3) {
                button.setEnabled(false);
            }
        } else {
            for (JButton button : listOf4x4) {
                button.setEnabled(false);
            }
        }

    }

    private boolean checkWinCondition() {
        lblMove.setText("" + moves);
        if (cbboxSize.getSelectedIndex() == 0) {
            numberList.clear();
            for (int i = 1; i < 9; i++) {
                numberList.add("" + i);
                if (!listOf3x3.get(i - 1).getText().equals(numberList.get(i - 1))) {
                    return false;
                }
            }
            return true;
        } else {
            numberList.clear();
            for (int i = 1; i < 16; i++) {
                numberList.add("" + i);
                if (!listOf4x4.get(i - 1).getText().equals(numberList.get(i - 1))) {
                    return false;
                }
            }
            return true;
        }

    }

