    private void btnNewGameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewGameActionPerformed
        // TODO add your handling code here:
        //3x3 Board
        moves = 0;
        lblMove.setText(""+moves);
        startTime = System.currentTimeMillis();
        if (cbboxSize.getSelectedIndex() == 0) {
            numberList.clear();
            for (int i = 1; i < 9; i++) {
                numberList.add("" + i);
            }
            //Add 1 blank button
            numberList.add("");

            //Random number for a new game
            Collections.shuffle(numberList);

            for (int i = 0; i < 9; i++) {
                listOf3x3.get(i).setText(numberList.get(i));
            }

            for (JButton button : listOf3x3) {
                button.setEnabled(true);
            }
        } //4x4 Board
        else {
            numberList.clear();
            for (int i = 1; i < 16; i++) {
                numberList.add("" + i);
            }
            numberList.add("");
            Collections.shuffle(numberList);

            for (int i = 0; i < 16; i++) {
                listOf4x4.get(i).setText(numberList.get(i));
            }
            for (JButton button : listOf4x4) {
                button.setEnabled(true);
            }
        }
        running = true;
       
    }//GEN-LAST:event_btnNewGameActionPerformed

